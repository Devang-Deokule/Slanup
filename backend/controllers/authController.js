const User = require('../models/User');
const jwt = require('jsonwebtoken');

// Generate JWT Token
const generateToken = (id) => {
  return jwt.sign({ id }, process.env.JWT_SECRET || 'slanup_secret_key_change_in_production', {
    expiresIn: '30d'
  });
};

